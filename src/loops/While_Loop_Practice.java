package loops;

public class While_Loop_Practice {

	public static void main(String[] args) {
		
		//I want print even number
		
		int a=2;
		while(a<51)
		{
			System.out.println(a);
			a=a+2;
		}
		System.out.println("======================");
		//I want print odd number
		
		int b=1;
		while (b<=51)
		{
			System.out.println(b);
			b=b+2;
		}
	}

}
