package loops;

public class Do_While_Study {

	public static void main(String[] args) {
		
		
		//I want to table of 10 
		//initialization-->10
		//condition--><=100
		// updation--> i=i+10
		
		int m=10;
		do
		{
			System.out.println(m);//10,20,30....90,100
			m=m+10;//20,30,40....100,110
		}
		while(m<=100);//20,30
	}

}
